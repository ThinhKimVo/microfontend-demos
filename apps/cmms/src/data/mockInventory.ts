export const getInventoryItemById = (id: string): InventoryItem | undefined => {
  return mockInventory.find(item => item.id === id)
}

export const getLowStockItems = (): InventoryItem[] => {
  return mockInventory.filter(item => item.currentQuantity <= item.minimumLevel)
}

export const getInventoryByCategory = (category: string): InventoryItem[] => {
  return mockInventory.filter(item => item.mepCategory === category)
}

export const getTransactionsByItem = (itemId: string): InventoryTransaction[] => {
  return mockInventoryTransactions.filter(trx => trx.itemId === itemId)
}
