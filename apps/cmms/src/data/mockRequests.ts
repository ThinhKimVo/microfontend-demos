export const getRequestById = (id: string): ServiceRequest | undefined => {
  return mockRequests.find(req => req.id === id)
}

export const getRequestsByBuilding = (buildingId: string): ServiceRequest[] => {
  return mockRequests.filter(req => req.buildingId === buildingId)
}

export const getRequestsByStatus = (status: string): ServiceRequest[] => {
  return mockRequests.filter(req => req.status === status)
}

export const getPendingRequests = (): ServiceRequest[] => {
  return mockRequests.filter(req =>
    req.status === 'New' || req.status === 'Acknowledged'
  )
}

export const getRequestsNotConverted = (): ServiceRequest[] => {
  return mockRequests.filter(req => !req.convertedToWO)
}
