export const getVendorById = (id: string): Vendor | undefined => {
  return mockVendors.find(vendor => vendor.id === id)
}

export const getVendorsBySpecialty = (specialty: string): Vendor[] => {
  return mockVendors.filter(vendor => vendor.mepSpecialties.includes(specialty as any))
}

export const getVendorsWithExpiringContracts = (daysThreshold: number = 90): Vendor[] => {
  const today = new Date()
  const thresholdDate = new Date(today.getTime() + daysThreshold * 24 * 60 * 60 * 1000)

  return mockVendors.filter(vendor => {
    if (!vendor.contractEndDate) return false
    const endDate = new Date(vendor.contractEndDate)
    return endDate <= thresholdDate
  })
}
